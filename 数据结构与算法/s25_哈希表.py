"""
    哈希表：一个通过哈希函数来计算数据存储位置的数据结构，通常支持如下操作：
        insert(key,value)：插入键值对(key,value)
        get(key)：如果存在键为key的键值对，则返回其value，否则返回空值
        delete(key)：删除键为key的键值对

    直接寻址表：当关键字的全域U比较小时，直接寻址时一种简单而有效的方式
        注：直接寻址表不是哈希，也不算哈希的一种
        - 本质：**数组（列表）**，下标 = 关键字 key
        - 结构：**无键值对**，仅标记关键字是否存在
        - 操作：
          - 插入：`T[key] = 存在`
          - 查询：直接看 `T[key]`
          - 删除：`T[key] = 不存在`
        - 复杂度：均为 **O(1)**
        - 特点：极快，但关键字范围大时**空间浪费严重**
        - 用途：实现**集合**（判断元素是否存在）

    一、哈希（Hash）
        1. 定义
           - 任意长度输入 → 固定长度输出
           - 输出叫“哈希值”
        2. 三个特点
           - 单向：不能从哈希值反推输入
           - 确定性：相同输入必得相同输出
           - 抗碰撞：不同输入尽量不同输出
        3. 常见算法
           - MD5（已不安全）
           - SHA-1（已弱）
           - SHA-256（常用）
        4. 主要用途
           - 密码存储
           - 文件完整性校验
           - 哈希表索引

    二、哈希表（Hash Table，又称为散列表）
        1. 定义
           - 一种数据结构
           - 用哈希函数把键映射到存储位置
        2. 核心操作（平均 O(1)）
           - 插入：put(key, value)
           - 查找：get(key)
           - 删除：remove(key)
        3. 原理示意
           - 数组 + 哈希函数
           - 步骤：
             ① 输入 key
             ② 计算哈希值
             ③ 取模得到数组下标
             ④ 存取数据
        4. 哈希冲突（Collision）
           - 不同 key 得到相同下标
           - 解决方法：
             - 链地址法（拉链法）：每个位置挂一个链表
             - 开放地址法：找下一个空位（线性探测、二次探测、二度哈希）
             - 再哈希法：用第二个哈希函数
        5. 重要概念
           - 负载因子 = 元素个数 / 数组长度
           - 超过阈值（如 0.75）→ 扩容（rehash）
        6. 常见哈希函数
           - 除法哈希法：h(k) = k % m
           - 乘法哈希法：h(k) = floor(m * (A * key % 1))
           - 全域哈希法：hᵢ,ⱼ(k) = ((i * key + j) mod p) mod m   i,j = 1,2,...,p-1

"""