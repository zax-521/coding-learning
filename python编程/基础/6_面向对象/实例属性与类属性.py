"""
    属性分为：
        1.实例属性：实例属性属于每个具体对象的属性，每个对象都是独立的。(各个对象特有的属性)
            实例对象.属性
        2.类属性：类属性属于类本身的属性，所有实例共享的。(所有对象共享的数据或配置)
            类名.属性
        注：通过实例查找属性时，会先查找实例属性，如果实例属性不存在，再查找类属性。
            所以 也可以通过 '实例对象.属性' 调用类属性
    方法：
        实例方法：
            1.默认无装饰器
            2.用self指代当前实例对象
            3.可访问实例属性和类属性
        类方法：
            1.用@classmethod装饰器
            2.用cla指代当前类
            3.仅可访问类属性
        静态方法：
            1.用@staticmethod装饰器
            2.无默认参数
            3.不可访问类属性和实例属性(独立于类和实例)
"""